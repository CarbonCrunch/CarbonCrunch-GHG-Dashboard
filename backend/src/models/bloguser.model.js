import mongoose from "mongoose";

const blogUserSchema = mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
    },
    password: {
        type: String,
        required: true,
    },
})

const BlogUser = mongoose.model('BlogUser', blogUserSchema);
export default BlogUser;